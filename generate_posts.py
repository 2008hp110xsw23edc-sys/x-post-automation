import anthropic
import os
from datetime import datetime

GUIDE = """
## 発信者プロフィール
- 30代男性、3歳の娘持ち、平凡なサラリーマン
- 根回し・自己アピールが苦手で寡黙にコツコツこなすタイプ
- 同期が出世していくのにもやもやしている
- 月200万円の副業収入を目指し、45歳脱サラが目標
- 毎日のストレス・人間関係を我慢しながら生きるのをやめたい

## ターゲット読者
- 職場での根回し・アピールが苦手で悩んでいるサラリーマン
- 真面目にやっているのに評価されないと感じている人
- 副業・収益化に興味があるが何から始めればいいかわからない人
- サラリーマン生活に限界を感じて脱出口を探している人
- 人生に疲れている・悩んでいる人

## コンテンツのアプローチ・トーン
- 親しみやすく、やさしい語り口。ですます調
- 「華やかな成功者」ではなく「同じ悩みを持つ等身大の先行者」として発信
- 生成文の最後には必ず前向きなコメントを入れる
- 上から目線・成功自慢・マウント的な表現はしない

## 投稿フォーマット
- ツリー形式（2投稿セット）
- 1投稿目：ネガティブな内容（悩み・現実・葛藤）＋最後に「2投稿目とは？」↓
- 2投稿目：ポジティブな内容（気づき・転換・行動）＋最後にまとめのひと言
- 箇条書き・改行を多用しスマホで読みやすく
- 絵文字を1投稿2〜4個使う
- 1投稿は140字以内
- キャッチーでフックのある書き出し
- ハッシュタグなし
"""

PROMPT = f"""
以下のガイドラインに従って、X（Twitter）投稿を生成してください。

{GUIDE}

以下の3タイプをそれぞれ2セット、合計6セット生成してください。

【共感型】×2セット：読者の悩みや感情を言語化する
【ストーリー型】×2セット：自分の経験・進捗・葛藤をリアルに語る
【問いかけ型】×2セット：読者に「自分ごと」として考えさせる

出力形式：
---
## 共感型 セット1

【1投稿目】
（本文）

【2投稿目】
（本文）

---
## 共感型 セット2
...（以下同様）
"""


def main():
    client = anthropic.Anthropic(api_key=os.environ["ANTHROPIC_API_KEY"])

    message = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=4000,
        messages=[{"role": "user", "content": PROMPT}],
    )

    posts = message.content[0].text
    today = datetime.now().strftime("%Y-%m-%d")
    filename = f"posts/{today}.md"

    with open(filename, "w", encoding="utf-8") as f:
        f.write(f"# X投稿案 {today}\n\n")
        f.write("> このファイルを確認・修正してからXに投稿してください。\n\n")
        f.write(posts)

    print(f"生成完了: {filename}")


if __name__ == "__main__":
    main()
